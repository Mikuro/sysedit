package ru.asuprofi.viewModel;

public class RectCords {
    public double x;
    public double y;
    public double w;
    public double h;

    public RectCords() {
        this.x = 0;
        this.y = 0;
        this.w = 0;
        this.h = 0;
    }

    public RectCords(double x, double y, double w, double h) {
        this.x = x;
        this.y = y;
        this.w = w;
        this.h = h;
    }
}
